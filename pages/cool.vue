<script lang="js" setup>
useHead({
  link: [
    {
      rel: "stylesheet",
      href: "https://cdn.jsdelivr.net/npm/locomotive-scroll@3.5.4/dist/locomotive-scroll.min.css"
    }
  ]
});
onMounted(() => {
  setTimeout(() => {

      // Value setters
  const valueSetters = () => {
    gsap.set('#nav a', { y: '-100%', opacity: 0 });
    gsap.set('#home .parent .child', { y: '100%' });
    gsap.set('#home .row .icon', { opacity: 0 });
    document.querySelectorAll('#Visual>g').forEach(function (e) {
      // var character = e.childNodes[1]?.childNodes[1];
      var character = e.querySelector('g').children[0];
      character.style.strokeDasharray = character.getTotalLength() + 'px';
      character.style.strokeDashoffset = character.getTotalLength() + 'px';
    })
  }
    
  //Reveal to span
  document.querySelectorAll('.reveal')
  .forEach(function (elem) {
    let spanParent = document.createElement('span');
    let spanChild = document.createElement('span');

    spanParent.classList.add('parent');
    spanChild.classList.add('child');

    spanChild.innerHTML = elem.innerHTML;
    spanParent.appendChild(spanChild);

    elem.innerHTML = ''
    elem.appendChild(spanParent);

    valueSetters();
  });

  // load svg animation
  const animateSvg = () => {
    gsap.to('#Visual>g>g>path, #Visual>g>g>polyline', {
      strokeDashoffset: 0,
      duration: 2,
      delay: -0.5,
      ease: Expo.easeInOut,
    })
  }

  // load home page
  const animateHomePage = () => {
    var tlhp = gsap.timeline();
    tlhp
    .to('#nav a', {
      y: 0,
      opacity: 1,
      stagger: 0.05,
      ease: Expo.easeInOut
    })
    .to('#home .parent .child', {
      y: 0,
      stagger: 0.05,
      duration: 1.5,
      delay: -0.5,
      ease: Expo.easeInOut
    })
    .to('#home .row .icon', {
      opacity: 1,
      delay: -0.5,
      ease: Expo.easeInOut,
      onComplete: function () {
        animateSvg();
      }
    })
    .to('#imagery', {
      opacity: 1,
      delay: -0.5,
      ease: Expo.easeInOut
    })
  }

  // Load animation
  const loaderAnimation = () => {
    var tl = gsap.timeline()
  tl
    .from('#loader .child span', {
      x: 100,
      duration: 1.4,
      stagger: 0.2,
      ease: Power3.easeInOut
    })
    .to('#loader .parent .child', {
      y: '-100%',
      duration: 1,
      ease: Circ.easeInOut
    })
    .to('#loader', {
      height: '0',
      duration: 1,
      ease: Circ.easeInOut,
    })
    .to('#green', {
      height: '100%',
      top: 0,
      duration: 1,
      delay: -0.8,
      ease: Circ.easeInOut
    })
    .to('#green', {
      height: '0%',
      duration: 1,
      delay: -0.5,
      ease: Circ.easeInOut,
      onComplete: function () {
        animateHomePage()
      }
    })
  }

  const locoInitialize = () => {
    const scroll = new LocomotiveScroll({
      el: document.querySelector('#main'),
      smooth: true
    });
  }

  const cardHoverEffect = () => {
    document.querySelectorAll('.cnt')
    .forEach(function (cnt) {
      var showingImage;
      const iamges = document.querySelector('#images')
      cnt.addEventListener('mousemove', function (dets) {
        // var xPos= dets.clientX;

        // var yPos= dets.clientY;
        // console.log(xPos, yPos)
        showingImage = dets.target;
        document.querySelector('#cursor').children[dets.target.dataset.index]
          .setAttribute('style', `opacity: 1; transform: translate(${dets.clientX}px, ${dets.clientY + iamges.offsetTop + iamges.offsetHeight + 340}px)`);
        showingImage.setAttribute('style', 'filter: grayscale(1)');
        document.querySelector('#work').setAttribute('style', `background-color: #${dets.target.dataset.color}`);
      })
      cnt.addEventListener('mouseleave', function (dets) {
        document.querySelector('#cursor').children[showingImage.dataset.index]
          .setAttribute('style', 'opacity: 0');
        showingImage.setAttribute('style', 'filter: grayscale(0)');
        document.querySelector('#work').setAttribute('style', 'background-color: #fff');
      })
    }) 
  }

  const cardSectionHoverEffect = () => {
    const cardSection = document.querySelector('#imagery');
    cardSection.addEventListener('mouseenter', () => {
      const imgRightContainer = document.querySelectorAll('#imgrig')
      imgRightContainer.forEach(function (elem) {
        const firstElement = elem.children[0]
        const secondElement = elem.children[1]
        const thirdElement = elem.children[2]

        firstElement.setAttribute('style', 'transform: translate(-40%, -10%) rotate(-10deg)')
        secondElement.setAttribute('style', 'transform: translate(24%, -6%) rotate(0deg)')
        thirdElement.setAttribute('style', 'transform: translate(85%, 20%) rotate(10deg)')
      })
    })
    cardSection.addEventListener('mouseleave', () => {
      const imgRightContainer = document.querySelectorAll('#imgrig')
      imgRightContainer.forEach(function (elem) {
        const firstElement = elem.children[0]
        const secondElement = elem.children[1]
        const thirdElement = elem.children[2]

        firstElement.setAttribute('style', 'transform: translate(-40%, -10%) rotate(-20deg)')
        secondElement.setAttribute('style', 'transform: translate(-15%, 0%) rotate(-15deg)')
        thirdElement.setAttribute('style', 'transform: translate(20%, 10%) rotate(-5deg)')
      })
    })
  }

  loaderAnimation();
  locoInitialize();
  cardHoverEffect();
  cardSectionHoverEffect();
}, 0);

})
